package geek1;

public class oneToTen {

	public static void main(String[] args) {
		int i=1,j=10;
		while(i<=10)
		{
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
		System.out.println("Reverse Order");
		//Reverse Order
		while(j>=1)
		{
			System.out.print(j+" ");
			j--;
		}
	}

}
